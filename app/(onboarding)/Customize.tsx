import React, { useCallback, useEffect, useMemo, useRef, useState } from 'react';
import { Alert, Image, ImageSourcePropType, ScrollView, StyleSheet, Text, TextInput, TouchableOpacity, View } from 'react-native';
import { useNavigation } from '@react-navigation/native';
import { useRouter } from 'expo-router';
import { Play, Shuffle } from 'lucide-react-native';
import GlassActionButton from '@/components/onboarding/GlassActionButton';
import GlassPanel from '@/components/onboarding/GlassPanel';
import OnboardingScreenShell from '@/components/onboarding/OnboardingScreenShell';
import OnboardingTopBar from '@/components/onboarding/OnboardingTopBar';
import { useGame } from '@/contexts/GameContext';
import { getOnboardingTheme } from '@/lib/config/onboardingTheme';
import { generateRandomName } from '@/src/features/onboarding/nameData';
import { useOnboarding } from '@/src/features/onboarding/OnboardingContext';
import { fontScale, responsiveBorderRadius, responsiveSpacing, scale, verticalScale } from '@/utils/scaling';

type SexOption = 'male' | 'female' | 'random';
type SexualityOption = 'straight' | 'gay' | 'bi';

interface SexOptionConfig {
  value: SexOption;
  label: string;
  icon: ImageSourcePropType;
}

interface SexualityOptionConfig {
  value: SexualityOption;
  label: string;
}

interface OnboardingIdentityStateShape {
  firstName: string;
  lastName: string;
  sex: SexOption;
  sexuality: SexualityOption;
}

interface SexChangeRegenerationArgs {
  sex: SexOption;
  firstName: string;
  lastName: string;
  lastAutoGeneratedSex: SexOption | null;
}

const BACKGROUND = require('@/assets/images/Main_Menu_2.png');

const SEX_OPTIONS: SexOptionConfig[] = [
  { value: 'male', label: 'Male', icon: require('@/assets/images/Sex/Male.png') },
  { value: 'female', label: 'Female', icon: require('@/assets/images/Sex/Female.png') },
  { value: 'random', label: 'Random', icon: require('@/assets/images/Sex/Dice.png') },
];

const SEXUALITY_OPTIONS: SexualityOptionConfig[] = [
  { value: 'straight', label: 'Straight' },
  { value: 'gay', label: 'Gay' },
  { value: 'bi', label: 'Bisexual' },
];

const formatLabel = (value: string): string => {
  if (!value) {
    return 'Unknown';
  }
  return value.charAt(0).toUpperCase() + value.slice(1);
};

export const shouldGenerateInitialIdentityName = (firstName: string, lastName: string): boolean => {
  return !firstName && !lastName;
};

export const shouldRegenerateIdentityNameForSexChange = ({
  sex,
  firstName,
  lastName,
  lastAutoGeneratedSex,
}: SexChangeRegenerationArgs): boolean => {
  const isExplicitSex = sex === 'male' || sex === 'female';
  if (!isExplicitSex) {
    return false;
  }
  if (!firstName && !lastName) {
    return false;
  }
  return lastAutoGeneratedSex !== sex;
};

export const applyIdentityDraftToOnboardingState = <T extends OnboardingIdentityStateShape>(
  previousState: T,
  identityDraft: OnboardingIdentityStateShape
): T => {
  return {
    ...previousState,
    ...identityDraft,
  };
};

export default function Customize() {
  const router = useRouter();
  const navigation = useNavigation();
  const { gameState } = useGame();
  const { state, setState } = useOnboarding();
  const [firstName, setFirstName] = useState(state.firstName || '');
  const [lastName, setLastName] = useState(state.lastName || '');
  const [sex, setSex] = useState<SexOption>(state.sex || 'random');
  const [sexuality, setSexuality] = useState<SexualityOption>(state.sexuality || 'straight');
  const lastAutoGeneratedSex = useRef<SexOption | null>(null);

  const isDarkMode = Boolean(gameState?.settings?.darkMode);
  const theme = getOnboardingTheme(isDarkMode);

  useEffect(() => {
    if (!shouldGenerateInitialIdentityName(firstName, lastName)) {
      return;
    }

    const randomName = generateRandomName(sex);
    setFirstName(randomName.firstName);
    setLastName(randomName.lastName);
    lastAutoGeneratedSex.current = sex;
  }, [firstName, lastName, sex]);

  useEffect(() => {
    if (
      !shouldRegenerateIdentityNameForSexChange({
        sex,
        firstName,
        lastName,
        lastAutoGeneratedSex: lastAutoGeneratedSex.current,
      })
    ) {
      return;
    }

    const randomName = generateRandomName(sex);
    setFirstName(randomName.firstName);
    setLastName(randomName.lastName);
    lastAutoGeneratedSex.current = sex;
  }, [firstName, lastName, sex]);

  const handleBack = useCallback(() => {
    if (navigation.canGoBack()) {
      router.back();
      return;
    }
    router.replace('/(onboarding)/MainMenu');
  }, [navigation, router]);

  const handleShuffle = useCallback(() => {
    const randomName = generateRandomName(sex);
    setFirstName(randomName.firstName);
    setLastName(randomName.lastName);
    lastAutoGeneratedSex.current = sex;
  }, [sex]);

  const handleContinue = useCallback(() => {
    setState((prev) =>
      applyIdentityDraftToOnboardingState(prev, {
        firstName,
        lastName,
        sex,
        sexuality,
      })
    );
    router.push('/(onboarding)/Perks');
  }, [firstName, lastName, router, setState, sex, sexuality]);

  const selectedSexLabel = useMemo(() => {
    return SEX_OPTIONS.find((option) => option.value === sex)?.label ?? formatLabel(sex);
  }, [sex]);

  const selectedSexualityLabel = useMemo(() => {
    return SEXUALITY_OPTIONS.find((option) => option.value === sexuality)?.label ?? formatLabel(sexuality);
  }, [sexuality]);

  const renderNameSection = () => {
    return (
      <GlassPanel style={styles.sectionPanel}>
        <View style={styles.sectionHeader}>
          <Text style={[styles.sectionTitle, { color: theme.title }]}>Name</Text>
          <TouchableOpacity
            accessibilityRole="button"
            onPress={handleShuffle}
            style={[styles.shuffleButton, { borderColor: theme.glassBorder }]}
          >
            <Shuffle size={scale(16)} color={theme.accentText} />
            <Text style={[styles.shuffleLabel, { color: theme.accentText }]}>Shuffle</Text>
          </TouchableOpacity>
        </View>

        <View style={styles.nameRow}>
          <View style={styles.nameField}>
            <Text style={[styles.inputLabel, { color: theme.subtitle }]}>First Name</Text>
            <View style={[styles.inputWrap, { borderColor: theme.glassBorder }]}>
              <TextInput
                placeholder="Enter first name"
                placeholderTextColor={theme.subtitle}
                style={[styles.inputText, { color: theme.title }]}
                value={firstName}
                onChangeText={setFirstName}
              />
            </View>
          </View>

          <View style={styles.nameField}>
            <Text style={[styles.inputLabel, { color: theme.subtitle }]}>Last Name</Text>
            <View style={[styles.inputWrap, { borderColor: theme.glassBorder }]}>
              <TextInput
                placeholder="Enter last name"
                placeholderTextColor={theme.subtitle}
                style={[styles.inputText, { color: theme.title }]}
                value={lastName}
                onChangeText={setLastName}
              />
            </View>
          </View>
        </View>
      </GlassPanel>
    );
  };

  const renderSexSection = () => {
    return (
      <GlassPanel style={styles.sectionPanel}>
        <Text style={[styles.sectionTitle, { color: theme.title }]}>Sex</Text>
        <View style={styles.sexRow}>
          {SEX_OPTIONS.map((option) => {
            const isSelected = sex === option.value;
            return (
              <TouchableOpacity
                key={option.value}
                accessibilityRole="button"
                onPress={() => setSex(option.value)}
                style={[
                  styles.sexCard,
                  isSelected
                    ? { borderColor: 'rgba(52, 211, 153, 0.62)', backgroundColor: 'rgba(16, 185, 129, 0.2)' }
                    : { borderColor: theme.glassBorder },
                ]}
              >
                <Image source={option.icon} style={styles.sexIcon} />
                <Text style={[styles.sexLabel, { color: theme.title }]}>{option.label}</Text>
              </TouchableOpacity>
            );
          })}
        </View>
      </GlassPanel>
    );
  };

  const renderSexualitySection = () => {
    return (
      <GlassPanel style={styles.sectionPanel}>
        <Text style={[styles.sectionTitle, { color: theme.title }]}>Sexuality</Text>
        <View style={styles.sexualityRow}>
          {SEXUALITY_OPTIONS.map((option) => {
            const isSelected = sexuality === option.value;
            return (
              <TouchableOpacity
                key={option.value}
                accessibilityRole="button"
                onPress={() => setSexuality(option.value)}
                style={[
                  styles.sexualityChip,
                  isSelected
                    ? { borderColor: 'rgba(96, 165, 250, 0.66)', backgroundColor: 'rgba(59, 130, 246, 0.2)' }
                    : { borderColor: theme.glassBorder },
                ]}
              >
                <Text style={[styles.sexualityLabel, { color: theme.title }]}>{option.label}</Text>
              </TouchableOpacity>
            );
          })}
        </View>
      </GlassPanel>
    );
  };

  return (
    <OnboardingScreenShell backgroundSource={BACKGROUND}>
      <OnboardingTopBar
        title="Create Identity"
        subtitle="Set your name, sex, and sexuality before choosing perks."
        onBack={handleBack}
        onInfo={() =>
          Alert.alert(
            'Create Your Identity',
            'Customize your character by choosing their name, sex, and sexuality. Use Shuffle to generate names instantly.'
          )
        }
      />

      <ScrollView contentContainerStyle={styles.scrollContent} showsVerticalScrollIndicator={false}>
        <GlassPanel strong style={styles.heroPanel}>
          <Text style={[styles.heroTitle, { color: theme.title }]}>Identity Setup</Text>
          <Text style={[styles.heroSubtitle, { color: theme.subtitle }]}>
            Your identity choices carry into perks and your first in-game profile.
          </Text>
        </GlassPanel>

        {renderNameSection()}
        {renderSexSection()}
        {renderSexualitySection()}
      </ScrollView>

      <GlassActionButton
        highlighted
        icon={<Play color={theme.title} size={scale(22)} />}
        onPress={handleContinue}
        subtitle={`${selectedSexLabel} • ${selectedSexualityLabel} • Save identity and continue`}
        title="Continue To Perks"
      />
    </OnboardingScreenShell>
  );
}

