import React, { useCallback, useEffect, useMemo, useRef, useState } from 'react';
import { Alert, Image, ImageSourcePropType, Platform, ScrollView, StyleSheet, Text, TextInput, TouchableOpacity, View } from 'react-native';
import { useNavigation } from '@react-navigation/native';
import { useRouter } from 'expo-router';
import { Play, Shuffle } from 'lucide-react-native';
import BlurViewFallback from '@/components/fallbacks/BlurViewFallback';
const BlurView = BlurViewFallback;
import LinearGradientFallback from '@/components/fallbacks/LinearGradientFallback';
const LinearGradient = LinearGradientFallback;
import OnboardingScreenShellV2 from '@/components/onboarding/OnboardingScreenShellV2';
import OnboardingGlassHeader from '@/components/onboarding/OnboardingGlassHeader';
import OnboardingFloatingButton from '@/components/onboarding/OnboardingFloatingButton';
import OnboardingStepBar from '@/components/onboarding/OnboardingStepBar';
import { useGame } from '@/contexts/GameContext';
import { generateRandomName } from '@/src/features/onboarding/nameData';
import {
  applyIdentityDraftToOnboardingState,
  canContinueFromIdentityDraft,
  IdentitySex,
  IdentitySexuality,
  shouldGenerateInitialIdentityName,
  shouldRegenerateIdentityNameForSexChange,
} from '@/src/features/onboarding/customizeIdentity';
import { useOnboarding } from '@/src/features/onboarding/OnboardingContext';
import { logOnboardingStepView } from '@/src/features/onboarding/onboardingAnalytics';
import { useOnboardingFlowGuard } from '@/hooks/useOnboardingFlowGuard';
import { haptic } from '@/utils/haptics';
import {
  fontScale,
  responsiveBorderRadius,
  responsiveFontSize,
  responsivePadding,
  responsiveSpacing,
  scale,
  verticalScale,
} from '@/utils/scaling';

type SexOption = IdentitySex;
type SexualityOption = IdentitySexuality;

interface SexOptionConfig {
  value: SexOption;
  label: string;
  icon: ImageSourcePropType;
}

interface SexualityOptionConfig {
  value: SexualityOption;
  label: string;
}

const SEX_OPTIONS: SexOptionConfig[] = [
  { value: 'male', label: 'Male', icon: require('@/assets/images/Sex/Male.png') },
  { value: 'female', label: 'Female', icon: require('@/assets/images/Sex/Female.png') },
  { value: 'random', label: 'Random', icon: require('@/assets/images/Sex/Dice.png') },
];

const SEXUALITY_OPTIONS: SexualityOptionConfig[] = [
  { value: 'straight', label: 'Straight' },
  { value: 'gay', label: 'Gay' },
  { value: 'bi', label: 'Bisexual' },
];

export default function Customize() {
  const router = useRouter();
  const navigation = useNavigation();
  const { gameState } = useGame();
  const { state, setState } = useOnboarding();
  useOnboardingFlowGuard('Customize');

  useEffect(() => {
    logOnboardingStepView('Customize');
  }, []);

  const [firstName, setFirstName] = useState(state.firstName || '');
  const [lastName, setLastName] = useState(state.lastName || '');
  const [sex, setSex] = useState<SexOption>(state.sex || 'random');
  const [sexuality, setSexuality] = useState<SexualityOption>(state.sexuality || 'straight');
  const lastAutoGeneratedSex = useRef<SexOption | null>(null);

  useEffect(() => {
    if (!shouldGenerateInitialIdentityName(firstName, lastName)) {
      return;
    }

    const randomName = generateRandomName(sex);
    setFirstName(randomName.firstName);
    setLastName(randomName.lastName);
    lastAutoGeneratedSex.current = sex;
  }, [firstName, lastName, sex]);

  useEffect(() => {
    if (
      !shouldRegenerateIdentityNameForSexChange({
        sex,
        firstName,
        lastName,
        lastAutoGeneratedSex: lastAutoGeneratedSex.current,
      })
    ) {
      return;
    }

    const randomName = generateRandomName(sex);
    setFirstName(randomName.firstName);
    setLastName(randomName.lastName);
    lastAutoGeneratedSex.current = sex;
  }, [firstName, lastName, sex]);

  const handleBack = useCallback(() => {
    if (navigation.canGoBack()) {
      router.back();
      return;
    }
    router.replace('/(onboarding)/MainMenu');
  }, [navigation, router]);

  const handleShuffle = useCallback(() => {
    haptic.light();
    const randomName = generateRandomName(sex);
    setFirstName(randomName.firstName);
    setLastName(randomName.lastName);
    lastAutoGeneratedSex.current = sex;
  }, [sex]);

  const handleFirstNameChange = useCallback((value: string) => {
    lastAutoGeneratedSex.current = null;
    setFirstName(value);
  }, []);

  const handleLastNameChange = useCallback((value: string) => {
    lastAutoGeneratedSex.current = null;
    setLastName(value);
  }, []);

  const handleContinue = useCallback(() => {
    if (!state.scenario) {
      haptic.error();
      Alert.alert('Missing Scenario', 'Choose a scenario before customizing identity.');
      router.replace('/(onboarding)/Scenarios');
      return;
    }

    if (!canContinueFromIdentityDraft({ firstName, lastName })) {
      haptic.error();
      Alert.alert('Missing Name', 'Enter both first and last name to continue.');
      return;
    }

    haptic.medium();
    const trimmedFirstName = firstName.trim();
    const trimmedLastName = lastName.trim();

    setState((prev) =>
      applyIdentityDraftToOnboardingState(prev, {
        firstName: trimmedFirstName,
        lastName: trimmedLastName,
        sex,
        sexuality,
      })
    );
    router.push('/(onboarding)/Perks');
  }, [firstName, lastName, router, setState, sex, sexuality, state.scenario]);

  const selectedSexLabel = useMemo(() => {
    return SEX_OPTIONS.find((option) => option.value === sex)?.label ?? sex;
  }, [sex]);

  const selectedSexualityLabel = useMemo(() => {
    return SEXUALITY_OPTIONS.find((option) => option.value === sexuality)?.label ?? sexuality;
  }, [sexuality]);

  return (
    <OnboardingScreenShellV2
      floatingButton={
        <OnboardingFloatingButton
          title="Continue To Perks"
          onPress={handleContinue}
          icon={<Play size={24} color="#FFFFFF" />}
        />
      }
    >
      <OnboardingGlassHeader
        title="Create Identity"
        onBack={handleBack}
        onInfo={() =>
          Alert.alert(
            'Create Your Identity',
            'Customize your character by choosing their name, sex, and sexuality. Use Shuffle to generate names instantly.'
          )
        }
      />

      <OnboardingStepBar currentStep={2} totalSteps={3} />

      <Text style={styles.guidanceText}>
        Set up your character. These choices shape your social life and relationships.
      </Text>

      <ScrollView
        style={styles.scrollContainer}
        contentContainerStyle={styles.scrollContent}
        showsVerticalScrollIndicator={true}
      >
        {/* Name Section */}
        <View style={styles.sectionContainer}>
          <BlurView intensity={20} style={styles.sectionBlur}>
            <LinearGradient
              colors={['rgba(31, 41, 55, 0.8)', 'rgba(17, 24, 39, 0.8)']}
              start={{ x: 0, y: 0 }}
              end={{ x: 1, y: 1 }}
              style={styles.sectionCard}
            >
              <View style={styles.sectionHeader}>
                <Text style={styles.sectionTitle}>Name</Text>
                <TouchableOpacity
                  accessibilityLabel="Shuffle name"
                  onPress={handleShuffle}
                  style={styles.shuffleButton}
                >
                  <Shuffle size={scale(16)} color="#60A5FA" />
                  <Text style={styles.shuffleLabel}>Shuffle</Text>
                </TouchableOpacity>
              </View>

              <View style={styles.nameRow}>
                <View style={styles.nameField}>
                  <Text style={styles.inputLabel}>First Name</Text>
                  <View style={styles.inputWrap}>
                    <TextInput
                      placeholder="Enter first name"
                      placeholderTextColor="#6B7280"
                      style={styles.inputText}
                      value={firstName}
                      onChangeText={handleFirstNameChange}
                    />
                  </View>
                </View>

                <View style={styles.nameField}>
                  <Text style={styles.inputLabel}>Last Name</Text>
                  <View style={styles.inputWrap}>
                    <TextInput
                      placeholder="Enter last name"
                      placeholderTextColor="#6B7280"
                      style={styles.inputText}
                      value={lastName}
                      onChangeText={handleLastNameChange}
                    />
                  </View>
                </View>
              </View>
            </LinearGradient>
          </BlurView>
        </View>

        {/* Sex Section */}
        <View style={styles.sectionContainer}>
          <BlurView intensity={20} style={styles.sectionBlur}>
            <LinearGradient
              colors={['rgba(31, 41, 55, 0.8)', 'rgba(17, 24, 39, 0.8)']}
              start={{ x: 0, y: 0 }}
              end={{ x: 1, y: 1 }}
              style={styles.sectionCard}
            >
              <Text style={styles.sectionTitle}>Sex</Text>
              <View style={styles.sexRow}>
                {SEX_OPTIONS.map((option) => {
                  const isSelected = sex === option.value;
                  return (
                    <TouchableOpacity
                      key={option.value}
                      accessibilityRole="button"
                      onPress={() => { haptic.selection(); setSex(option.value); }}
                      style={[
                        styles.sexCard,
                        isSelected && styles.sexCardSelected,
                      ]}
                    >
                      <Image source={option.icon} style={styles.sexIcon} />
                      <Text style={styles.sexLabel}>{option.label}</Text>
                    </TouchableOpacity>
                  );
                })}
              </View>
            </LinearGradient>
          </BlurView>
        </View>

        {/* Sexuality Section */}
        <View style={styles.sectionContainer}>
          <BlurView intensity={20} style={styles.sectionBlur}>
            <LinearGradient
              colors={['rgba(31, 41, 55, 0.8)', 'rgba(17, 24, 39, 0.8)']}
              start={{ x: 0, y: 0 }}
              end={{ x: 1, y: 1 }}
              style={styles.sectionCard}
            >
              <Text style={styles.sectionTitle}>Sexuality</Text>
              <View style={styles.sexualityRow}>
                {SEXUALITY_OPTIONS.map((option) => {
                  const isSelected = sexuality === option.value;
                  return (
                    <TouchableOpacity
                      key={option.value}
                      accessibilityRole="button"
                      onPress={() => { haptic.selection(); setSexuality(option.value); }}
                      style={[
                        styles.sexualityChip,
                        isSelected && styles.sexualityChipSelected,
                      ]}
                    >
                      <Text style={[styles.sexualityLabel, isSelected && styles.sexualityLabelSelected]}>
                        {option.label}
                      </Text>
                    </TouchableOpacity>
                  );
                })}
              </View>
            </LinearGradient>
          </BlurView>
        </View>

        <View style={{ height: 140 }} />
      </ScrollView>
    </OnboardingScreenShellV2>
  );
}

